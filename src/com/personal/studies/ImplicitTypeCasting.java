package com.personal.studies;

public class ImplicitTypeCasting {
	
	public static void main(String[] args) {
		
		byte a = 30;
		System.out.println("Byte vale --- "+a);
		
		short b = a;
		System.out.println("Short Value ---- "+b);
		
		int c = b;
		System.out.println("Integer Value --- "+c);
		
		float d = b;
		System.out.println("Float Value --- "+d);
		
		double e = d;
		System.out.println("Double Valuie --- "+e);
	}

}
